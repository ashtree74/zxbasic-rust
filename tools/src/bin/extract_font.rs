//! Reads vendor/zxrom/13_font.asm and emits crates/zxbasic-core/src/charset.rs
//! with `pub const FONT: [[u8; 8]; 96]`.
//!
//! Run from repo root: `cargo run -p zxbasic-tools --bin extract-font`

use std::fs;
use std::path::PathBuf;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .ok_or("no parent")?
        .to_path_buf();
    let src = root.join("vendor/zxrom/13_font.asm");
    let dst = root.join("crates/zxbasic-core/src/charset.rs");

    let text = fs::read_to_string(&src)?;
    let glyphs = parse_defb_glyphs(&text);
    assert_eq!(glyphs.len(), 96, "expected 96 glyphs, got {}", glyphs.len());

    let mut out = String::new();
    out.push_str(
        "//! ZX Spectrum 48K character font.\n\
         //!\n\
         //! Generated by `tools/src/bin/extract_font.rs` from\n\
         //! `vendor/zxrom/13_font.asm` — DO NOT EDIT BY HAND.\n\
         //!\n\
         //! 96 glyphs covering ASCII 32..=127. Each glyph is 8 bytes; each byte\n\
         //! is one row, MSB = leftmost pixel.\n\
         \n\
         /// First ASCII codepoint covered by [`FONT`].\n\
         pub const FONT_FIRST_CHAR: u8 = 32;\n\
         \n\
         /// Number of glyphs in [`FONT`] (codepoints 32..=127).\n\
         pub const FONT_GLYPH_COUNT: usize = 96;\n\
         \n\
         /// 8×8 monochrome glyph bitmaps for ASCII 32..=127.\n\
         pub const FONT: [[u8; 8]; FONT_GLYPH_COUNT] = [\n",
    );
    for (i, g) in glyphs.iter().enumerate() {
        let ch = (FONT_FIRST_CHAR as usize + i) as u8 as char;
        let ch_repr = if ch.is_ascii_graphic() && ch != '\\' && ch != '\'' {
            format!("'{}'", ch)
        } else {
            format!("{:#04x}", FONT_FIRST_CHAR as usize + i)
        };
        out.push_str(&format!(
            "    [{}], // {}\n",
            g.iter()
                .map(|b| format!("0x{:02x}", b))
                .collect::<Vec<_>>()
                .join(", "),
            ch_repr,
        ));
    }
    out.push_str("];\n");

    fs::write(&dst, out)?;
    println!("wrote {} glyphs → {}", glyphs.len(), dst.display());
    Ok(())
}

const FONT_FIRST_CHAR: u8 = 32;

fn parse_defb_glyphs(text: &str) -> Vec<[u8; 8]> {
    let mut out = Vec::new();
    for raw in text.lines() {
        // Strip the `//` / `;` trailing comment.
        let line = raw
            .split_once(";")
            .map(|(a, _)| a)
            .unwrap_or(raw)
            .trim();
        // Look only at lines that start with `defb`.
        let Some(rest) = line.strip_prefix("defb") else {
            continue;
        };
        let bytes: Vec<u8> = rest
            .split(',')
            .map(str::trim)
            .filter(|s| !s.is_empty())
            .map(|tok| {
                let hex = tok.trim_start_matches('$');
                u8::from_str_radix(hex, 16).expect("bad hex byte in 13_font.asm")
            })
            .collect();
        if bytes.len() == 8 {
            let mut arr = [0u8; 8];
            arr.copy_from_slice(&bytes);
            out.push(arr);
        } else {
            panic!("defb line did not yield 8 bytes: {:?}", raw);
        }
    }
    out
}
